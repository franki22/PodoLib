class CRC16ARC
{
public:
	static unsigned short CRC(unsigned char* icp, int icnt)
	{
		int cnt = icnt;
		unsigned short icrc = 0;
		unsigned char * cp = icp;
		while(cnt--)
		 icrc = ((icrc >> 8) & 0xff) ^ crcTableModbus[(icrc & 0xff) ^ *cp++];
		return(icrc);
	}
};

