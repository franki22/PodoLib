class CRC16
{
public:
	CRC16(unsigned short polynomial, unsigned short init, unsigned short xor, bool reflect_input, bool reflect_output)
		: m_polynomial(polynomial)
		, m_init(init)
		, m_xor(xor)
		, m_reflect_input(reflect_input)
		, m_reflect_output(reflect_output)
	{
	}

protected:
	unsigned short m_polynomial;
	unsigned short m_init;
	unsigned short m_xor;
	bool m_reflect_input;
	bool m_reflect_output;

protected:
	inline unsigned short Reflect(unsigned short value)
	{
		unsigned short result = 0;
		for( int i = 0; i < 16; i++ )
		{
			if( value & 0x01 )
			{
				result |= (1 << ((16 - 1) - i));
			}

			value = (value >> 1);
		}

		return result;
	}

public:
	inline unsigned short CRC(const unsigned char* value, unsigned short size)
	{
		unsigned short crc = m_init;
		for( int i = 0; i < size; i++ )
		{
			unsigned char cp = m_reflect_input ? REFLECT_TABLE[value[i]] : value[i];
			crc ^= (cp << 8);

			for( int j = 0; j < 8; j++ )
			{
				crc = crc & 0x8000 ? (crc << 1) ^ m_polynomial : crc << 1;
			}
		}

		if( m_reflect_output )
		{
			crc = Reflect( crc );
		}

		crc = (crc ^ m_xor) & 0xFFFF;

		return crc;
	}
};


